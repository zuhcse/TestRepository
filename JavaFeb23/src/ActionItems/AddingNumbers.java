package ActionItems;

public class AddingNumbers {

	public static void main(String[] args) {
		//add all number from 5 to 15

		int sum = 0;
		for(int i=5; i<=15; i++)
		{
			sum = sum+i;
						
		}
		System.out.println("The result is: " +sum);
		
		//add all number from 0 to 15
		
		int sum1 = 0;
		int i =0;
		while (i<=15)
		{
			sum1 = sum1 + i;
			i++;
						
		}
		System.out.println("The result is: " +sum1);
		
	
		
		
		

	}

}
