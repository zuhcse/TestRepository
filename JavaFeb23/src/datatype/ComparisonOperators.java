package datatype;

public class ComparisonOperators {

	public static void main(String[] args) {
		
		String actual = "Google";
		String expected = "google";
		/* ==
		 * !=   not equal
		 * >
		 * <
		 * >=
		 * <=
		 */
		
		int a = 15;
		int b = 16;
		
		System.out.println(actual!=expected);
		System.out.println(a>b);//false
		System.out.println(a<b);//true
		

	}

}
