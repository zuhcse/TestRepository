package datatype;

public class JavaOperator {

	public static void main(String[] args) {
		/**There are 5 types of Java Operators
		 * 
		 * Arithmetic Operators
		 * Assignment
		 * Comparison/Relational Operators
		 * Logical Operators
		 * Bitwise Operators
		 *  
		 */
		
		// Learning Arithmetic Operators
		//They are +,-,*,/,%(Modulus),++(increment),--()Decrement
		
		int a=10;
		int b=5;
		int result;
		
		result = a + b;//addition
		System.out.println("Addional result is " + result);//15
		result = a - b;
		System.out.println("Subtraction result is " + result);//5
		result = a * b;
		System.out.println("Multiplication result is " + result);//50
		result = a / b;
		System.out.println("Division result is " + result);//2
		result = a % b;
		System.out.println("Modulus result is " + result);
		//System.out.println("Incremental result is " + a++);//?
		//System.out.println("Decremental result is " + a--);//?
		
		

	}

}
