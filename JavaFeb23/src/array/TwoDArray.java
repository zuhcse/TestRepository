package array;

public class TwoDArray {

	public static void main(String[] args) {
		//2d array is array between array 
		int [][] my2DArray = new int [2][3];
		
		my2DArray[0][0] = 5;
		my2DArray[0][1] = 6;
		my2DArray[0][2] = 7;
		
		my2DArray[1][0] = 8;
		my2DArray[1][1] = 8;
		my2DArray[1][2] = 3;
		
		System.out.println(my2DArray[1][1]);

		
		
		
		
		
		
		
		
		
		

	}

}
